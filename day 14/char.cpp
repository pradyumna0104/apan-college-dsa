#include <iostream>
using namespace std;
int main()
{
    char ch1 = 'a';
    cout << "a = " << int(ch1) << endl;
    char ch2 = 'A';
    cout << "A = " << int(ch2) << endl;
    char ch3 = '0';
    cout << "0 = " << int(ch3) << endl;
    char ch4 = '9';
    cout << "9 = " << int(ch4) << endl;
    return 0;
}